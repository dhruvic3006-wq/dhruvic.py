#positive indexing
from array import array
arr=array('i',[10,20,30,40,50])
print(arr[0])#first element
print(arr[2])#third element
print(arr[4])#fifth element
#negative indexing
from array import array
arr=array('i',[10,20,30,40,50])
print(arr[-1])#last element
print(arr[-2])#second element
print(arr[-5])#first element
